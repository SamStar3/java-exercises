package javaBasic;

public class swapNumber {
	public static void main(String[] args) {
	// numberOne  = 10
	// numberTwo = 20
//		print numberOne - 20
//		print numberTwo - 10 
		// using three variables
		int numOne = 10;
		int numTwo = 20;
		int numThree = 0;
		numThree = numOne;
		numOne = numTwo;
		numTwo = numThree;
		
		System.out.println(numOne);
		System.out.println(numTwo);
	}

}
